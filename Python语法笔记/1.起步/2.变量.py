#变量
message = "hello world!"
print(message)
#注意：print 变量名不需要加双引号，但在给变量赋值的时候右边的字符串需要双引号

#此时message即为变量，它储存了字符串hello world！

#想修改变量时，只需要知道顺序结构，即在后面再重新赋值一个即可

#变量的命名和使用：
    #变量名只能含有字母，数字和下划线。
    #变量名中不能含有空格，但可以使用下划线来间隔单词。
    #不能将python中的关键字和函数名作为变量名。
    #变量名应该既简短又具有描述性。
    #慎用小写字母l和大写字母O，它们易被看成1和0.